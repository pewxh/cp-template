bool checkPrime(int n){
    for(int i=2;i<=sqrt(n);i++)
        if(!(n%i))
            return false;
    return (n > 1);
}

set<int> primeSieve(int n){
    set<int> primes;
    bool *sieve = new bool[n+1];
    sieve[0] = sieve[1] = false;
    for(int i=2;i<=n;i++)
        sieve[i] = true;
    for(int i=2;i<=n;i++){
        if(sieve[i]){
            if(checkPrime(i)){ 
                primes.insert(i);
                for(int j = i*i ; j <= n ; j += i)
                    sieve[j] = false;
            }
        }
    }
    delete [] sieve;
    return primes;
}